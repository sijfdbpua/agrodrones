"""
AgroDrones — консольная симуляция дистанционного мониторинга полей.

Точка входа в приложение. Здесь собирается вся система (поле, база, дроны),
и запускается интерактивное консольное меню.

Сюжет (направление 4 — Агродроны и дистанционный мониторинг):
  Дрон-разведчик облетает поле, снимает данные с датчиков (влажность,
  здоровье растений, вредители), система находит проблемные зоны, расставляет
  приоритеты и отправляет рабочий дрон обработать их за один день.

Запуск:
    python main.py

Структура проекта:
    main.py            — этот файл (интерфейс и запуск)
    simulation.py      — движок симуляции (один рабочий день)
    algorithms.py      — сортировка зон и построение маршрута
    models/
        sensor.py      — датчик (данные клетки поля)
        zone.py        — проблемная зона (задача для дрона)
        field.py       — поле как сетка датчиков + обход сетки
        drone.py       — дроны (наследование) + очередь задач (deque)
        base.py        — база дронов + история облётов (связный список)
"""

from models.field import Field
from models.base import Base
from models.drone import ScoutDrone, SprayDrone
from simulation import Simulation


def build_system():
    """Создаёт и связывает все объекты системы. Возвращает Simulation."""
    # Поле 8x8 — достаточно для наглядной карты в консоли.
    field = Field(name="Поле №1 (пшеница)", rows=8, cols=8)

    # База с двумя дронами: разведчик + рабочий (демонстрируем наследование).
    base = Base(name="Станция Альфа")
    base.add_drone(ScoutDrone(name="Скаут-1", battery=100))
    base.add_drone(SprayDrone(name="Спрей-1", battery=250, tank=22))

    return Simulation(field, base)


MENU = """
============================================================
        AGRODRONES — мониторинг и обработка полей
============================================================
  1. Показать карту поля (состояние датчиков)
  2. Прожить один рабочий день (разведка + обработка)
  3. Показать статус дронов
  4. Показать историю облётов
  5. Новый цикл (зарядить дроны, обновить поле)
  0. Выход
------------------------------------------------------------
Выберите действие: """


def main():
    sim = build_system()
    print("Система инициализирована. Флот: разведчик + рабочий дрон.")

    while True:
        choice = input(MENU).strip()

        if choice == "1":
            print()
            print(sim.field.render())

        elif choice == "2":
            print(sim.run_day(threshold=110, verbose=True))

        elif choice == "3":
            print("\nСтатус дронов:")
            for d in sim.base.drones:
                print("  " + d.status())

        elif choice == "4":
            print()
            print(sim.base.show_history())

        elif choice == "5":
            sim.refuel_and_recharge()
            print("\nДроны заряжены, баки наполнены, поле обновлено. Можно начинать новый день.")

        elif choice == "0":
            print("Завершение работы. До встречи на защите!")
            break

        else:
            print("Неизвестная команда, попробуйте ещё раз.")


if __name__ == "__main__":
    main()
