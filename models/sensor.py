"""
Модуль с классом Sensor (датчик).

Датчик — это источник данных в нашей системе. Дрон облетает поле и
"снимает показания" с виртуальных датчиков, расставленных по сетке поля.
Каждый датчик измеряет три параметра: влажность почвы, индекс здоровья
растений (NDVI-подобный показатель) и заражённость вредителями.

На защите про этот класс можно сказать так:
"Sensor — это сущность-данные. Он инкапсулирует измерения одной клетки
поля и умеет сам оценивать, насколько ситуация в этой клетке проблемная.
Логику оценки я держу внутри объекта (а не снаружи), потому что это и есть
принцип инкапсуляции: данные и методы их обработки живут вместе."
"""


class Sensor:
    """Виртуальный датчик в одной клетке поля.

    Атрибуты:
        row, col   — координаты клетки в сетке поля (целые числа).
        moisture   — влажность почвы в процентах (0..100).
        health     — индекс здоровья растений (0..100), чем больше тем лучше.
        pest_level — уровень заражённости вредителями (0..100), чем меньше тем лучше.
    """

    def __init__(self, row, col, moisture, health, pest_level):
        # Координаты нужны, чтобы привязать показание к месту на поле.
        self.row = row
        self.col = col
        # Сами измерения. В реальности их бы прислал физический датчик,
        # у нас они генерируются случайно при создании поля.
        self.moisture = moisture
        self.health = health
        self.pest_level = pest_level

    def problem_score(self):
        """Считает "балл проблемности" клетки — одно число от 0 и выше.

        Чем выше балл, тем хуже дела в клетке и тем срочнее её надо
        обрабатывать. Именно по этому баллу мы потом сортируем зоны
        по приоритету облёта (это один из наших алгоритмов).

        Логика простая и прозрачная:
          - мало влаги  -> плохо (прибавляем недостаток воды);
          - низкое здоровье -> плохо (прибавляем недостаток здоровья);
          - много вредителей -> плохо (прибавляем уровень вредителей с весом).
        """
        score = 0
        # Если влажность ниже 40% — считаем это засухой и штрафуем.
        if self.moisture < 40:
            score += (40 - self.moisture)
        # Недостаток здоровья растений до идеальных 100.
        score += (100 - self.health)
        # Вредители — самый опасный фактор, поэтому даём ему вес 1.5.
        score += self.pest_level * 1.5
        return round(score, 1)

    def status(self):
        """Возвращает короткую текстовую метку состояния клетки.

        Используется для красивого вывода карты поля в консоли.
        """
        score = self.problem_score()
        if score >= 120:
            return "CRITICAL"   # всё плохо, срочно обрабатывать
        elif score >= 70:
            return "WARNING"    # есть проблемы, держать на контроле
        else:
            return "OK"         # клетка в порядке

    def symbol(self):
        """Один символ для отрисовки клетки на карте поля.

        # — критично, ! — тревога, . — норма.
        """
        return {"CRITICAL": "#", "WARNING": "!", "OK": "."}[self.status()]

    def __repr__(self):
        # Удобное представление при отладке: видно координаты и балл.
        return f"Sensor({self.row},{self.col}, score={self.problem_score()})"
