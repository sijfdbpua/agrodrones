"""
Модуль с классом Zone (зона облёта).

Когда дрон проанализировал данные датчиков, он группирует проблемные
клетки в "зоны" — задачи, которые нужно выполнить. Zone — это, по сути,
единица работы для дрона: прилететь в точку (row, col), сделать действие
(полить / опрыскать / просто осмотреть) и закрыть задачу.

На защите:
"Zone — это класс-задача. Объекты Zone складываются в очередь задач на день.
У каждой зоны есть приоритет (берётся из problem_score датчика), тип
обработки и статус выполнения. Это пример того, как мы превращаем сырые
данные (показания датчиков) в управляемые бизнес-объекты."
"""


class Zone:
    """Проблемная зона поля = одна задача для дрона.

    Атрибуты:
        row, col   — где находится зона (координаты клетки).
        priority   — приоритет (чем больше, тем важнее). Берём из датчика.
        task_type  — тип обработки: "IRRIGATE" (полив), "SPRAY" (опрыскивание),
                     "INSPECT" (осмотр).
        done       — выполнена ли задача (True/False).
    """

    def __init__(self, row, col, priority, task_type):
        self.row = row
        self.col = col
        self.priority = priority
        self.task_type = task_type
        self.done = False

    def distance_to(self, row, col):
        """Манхэттенское расстояние от зоны до точки (row, col).

        Дрон летает по сетке, поэтому считаем расстояние как сумму
        разниц по строкам и столбцам (|dr| + |dc|). Это используется
        в алгоритме поиска ближайшей зоны (жадный маршрут дрона).
        """
        return abs(self.row - row) + abs(self.col - col)

    def complete(self):
        """Отмечает задачу выполненной."""
        self.done = True

    def __repr__(self):
        flag = "DONE" if self.done else "TODO"
        return (f"Zone({self.row},{self.col}) "
                f"{self.task_type} prio={self.priority} [{flag}]")
