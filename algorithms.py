"""
Модуль с алгоритмами проекта.

Здесь собраны АЛГОРИТМЫ 2 и 3 (алгоритм 1 — обход сетки — живёт в Field,
ближе к данным). Вынесены отдельно, чтобы на защите можно было открыть
один файл и показать "вот мои алгоритмы, вот их сложность".

  Алгоритм 2: сортировка зон по приоритету (сортировка слиянием, своя).
  Алгоритм 3: построение маршрута дрона жадным поиском ближайшей зоны.
"""


# ---------------------------------------------------------------------------
# АЛГОРИТМ 2: сортировка зон по приоритету (по убыванию).
# ---------------------------------------------------------------------------
# Я реализую сортировку слиянием (merge sort) вручную, а не вызываю
# встроенный sorted(), чтобы продемонстрировать понимание алгоритма.
# (В реальном коде, конечно, брали бы sorted с key — и это нормально,
#  но на хакатоне по структурам данных важно показать сам алгоритм.)
#
# Сложность: O(n log n) в любом случае — массив делится пополам log n раз,
# и на каждом уровне слияние проходит все n элементов. Это устойчивая
# сортировка, что приятно: зоны с равным приоритетом сохраняют исходный
# порядок (например, порядок обхода поля).

def merge_sort_zones(zones):
    """Сортирует список зон по убыванию приоритета. Возвращает новый список."""
    # База рекурсии: список из 0 или 1 элемента уже отсортирован.
    if len(zones) <= 1:
        return zones[:]

    mid = len(zones) // 2
    left = merge_sort_zones(zones[:mid])    # сортируем левую половину
    right = merge_sort_zones(zones[mid:])   # сортируем правую половину
    return _merge(left, right)              # сливаем две отсортированные части


def _merge(left, right):
    """Сливает два отсортированных (по убыванию) списка в один."""
    result = []
    i = j = 0
    while i < len(left) and j < len(right):
        # Берём тот элемент, у которого приоритет ВЫШЕ (сортировка по убыванию).
        if left[i].priority >= right[j].priority:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1
    # Один из списков кончился — дописываем "хвост" второго.
    result.extend(left[i:])
    result.extend(right[j:])
    return result


# ---------------------------------------------------------------------------
# АЛГОРИТМ 3: маршрут дрона — жадный поиск ближайшей зоны (nearest neighbour).
# ---------------------------------------------------------------------------
# Задача: дрон стоит в точке и должен облететь набор зон. В каком порядке
# лететь, чтобы суммарный путь был коротким? Идеальное решение (задача
# коммивояжёра) считается экспоненциально долго, поэтому используем жадную
# эвристику: каждый раз летим в ближайшую ещё не посещённую зону.
#
# Сложность: O(k^2), где k — число зон. На каждом из k шагов мы перебираем
# оставшиеся зоны (до k штук), чтобы найти ближайшую. Для масштабов хакатона
# (десятки зон) это быстро и наглядно.

def build_route(zones, start_row=0, start_col=0):
    """Возвращает зоны в порядке облёта по принципу 'ближайшая следующая'.

    zones — список зон для облёта (порядок входа не важен).
    start_row/col — откуда стартует дрон (обычно база в углу поля).
    """
    remaining = zones[:]      # копия, чтобы не портить исходный список
    route = []
    cur_r, cur_c = start_row, start_col

    while remaining:
        # Ищем ближайшую к текущей позиции зону (линейный поиск минимума).
        nearest = remaining[0]
        nearest_dist = nearest.distance_to(cur_r, cur_c)
        for z in remaining[1:]:
            d = z.distance_to(cur_r, cur_c)
            if d < nearest_dist:
                nearest = z
                nearest_dist = d
        # Перемещаемся в неё и убираем из оставшихся.
        route.append(nearest)
        cur_r, cur_c = nearest.row, nearest.col
        remaining.remove(nearest)

    return route
